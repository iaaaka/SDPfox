public class partialVolume {
    public AminoAcid aa = new AminoAcid();
    public double volumeFrac;

    public partialVolume() {
    }

    public partialVolume(AminoAcid a, double v) {
        aa.copy(a);
        volumeFrac = v;
    }
}
